#include <stdio.h>
#include <stdlib.h>

/**
	* main - entry point of the programme
	* Takes no argument
	*
	* Description: a program that prints the lowercase
	* alphabet in reverse, followed by a new line.
	*
	* Return: Programme always returns (0)
*/
int main(void)
{
	int i;

	for (i = 48; 8 <= 57; i++)
	{
		putchar(i);
	}
	putchar('\n');

	return (0);
}
